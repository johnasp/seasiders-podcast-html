<h3>What is the Seasiders Podcast?</h3>
<p>The Seasiders Podcast is a talk radio show  about the mighty Seasiders aka Blackpool FC and is curated by a group of Blackpool fans. The goal of this podcast is to inform, amuse and report on all the weekly goings on at Blomfield Road. We are totally independent and not linked to club in any way, we are just a few dudes in a room musing over the weekly trials and tribulations of Blackpool FC.
<p>We hope to get many other &lsquo;Pool fans and guests (hopefully some players and other people connected with the club) onto the show, so if you would like to get involved with the show on any level, be it presenting, production, research, writing, please <a href="mailto:john@seasiderspodcast.co.uk" data-mce-href="mailto:john.aspinall73@gmail.com">email us and tell us what you would like to get invovled in.</a></p>

