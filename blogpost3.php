<h4>Match action:</h4>

<iframe width="853" height="480" src="http://www.youtube.com/embed/BRdXaCB9T2w" frameborder="0" allowfullscreen></iframe>
<ul>
	<li>The pitch is abysmal once again.</li>
	<li>The Pool almost find themselves down a goal in the first minute when Martyn Woolford (Millwall) hits a shot but it goes off target.</li>
	<li>Rob Hulse was a nuisance for the Blackpool defenders throughout and the on-loan striker almost marked his first start for Millwall with a goal after 13 minutes.  Mark Halstead, making just his second start in goal for the hosts, spilled a cross into Hulse's path and he bundled his chance against the post before Blackpool scrambled clear.</li>
	<li><strong>GOAL</strong> (32') - The visitors were posing more headaches and it was no surprise when they broke the deadlock 13 minutes before the break, although the manner of the goal came in bizarre circumstances. Henry lined up another corner and whipped the ball in to the near post where it looped into the top corner with nobody seemingly touching it en route.</li>
	<li>The Bloomfield Road faithful have become used to seeing their side fall behind. Out of their 31 npower Championship contests this season they have now conceded first a staggering 21 times.</li>
	<li><strong>Halftime</strong>: Blackpool 0-1 Millwall.</li>
	<li>The Pool came back out of the locker room determined, and started off the second half well, dictating more the course of play than they had in the First Half.</li>
	<li><strong>Blackpool Sub</strong>: (57') - Matt Derbyshire for Filipe Tiago Gomes</li>
	<li><strong>GOAL</strong> (61') - Blackpool level, as Ince eventually produced a fantastic contribution just after the hour mark.  A deflected shot fell to the one man the Pool wanted it to and Ince took a couple of strides before rifling home his 17th goal of the season.</li>
	<li><strong>Blackpool Sub</strong>: (77') - Nathan Delfouneso for Matthew Phillips</li>
	<li><strong>GOAL</strong> (90' + 1) - Delfouneso ensured the hosts claimed all three points as he brought the ball down before finishing with aplomb past Forde. The Republic of Ireland international believed the on-loan Aston Villa forward had used his hand to control the ball, but the Officials didn't see it that way, and the Tangerine faithful went wild!</li>
	<li>It should be noted that Halstead was steadier and seemed to settle into the game more in the second half.</li>
	<li>The Seasiders then withstood a flurry of pressure from Millwall in the waning moments of stoppage time to claim the three points. A minimum of four minutes was posted on the board, but in the end stoppage lasted 5 plus minutes.</li>
</ul>

